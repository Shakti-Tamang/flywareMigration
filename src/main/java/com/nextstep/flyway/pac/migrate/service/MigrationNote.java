package com.nextstep.flyway.pac.migrate.service;

public class MigrationNote {
//    Versioned migration
//    Repetable Migration

//    In versioned migration in Flyway, each migration file is given a unique version
//    number (e.g., V1__initial.sql, V2__add_user_table.sql) and is applied in sequence.
//    It ensures migrations run only once and in order.
//
//    In repeatable migration, files are not versioned but can be run multiple
//    times whenever they change (e.g., R__update_views.sql). They are mainly used for things
//    like views, stored procedures, or functions that may need updates.

}

